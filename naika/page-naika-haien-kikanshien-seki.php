        <p>肺炎は、肺の組織が炎症を起こす病気で、主に細菌、ウイルス、あるいは真菌が原因です。気管支炎は、気管支の粘膜が炎症を起こす病気で、急性と慢性に分けられます。急性気管支炎は主にウイルス感染が原因で、慢性気管支炎は長期間のタバコの使用や大気汚染などが原因で起こります。これらの病気は、共通してせきを主な症状とします。</p>

        <h2>原因</h2>

        <p>肺炎の原因は多岐にわたりますが、最も一般的なのは細菌感染、特に肺炎球菌によるものです。ウイルス性肺炎も一般的で、特にインフルエンザウイルスや新型コロナウイルスが原因の場合があります。気管支炎の場合、急性は主にウイルス感染が原因ですが、慢性は長期間の喫煙や大気汚染、職業的な要因などが関係しています。</p>

        <h2>症状</h2>

