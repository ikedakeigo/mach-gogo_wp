        <h2>治療法</h2>

        <p>H. pylori感染の治療には、通常「三剤併用療法」と呼ばれる方法が用いられます。これは、2種類の抗生物質とプロトンポンプ阻害薬（胃酸の分泌を抑える薬）を組み合わせて服用する治療法です。治療期間は通常7〜14日間です。治療後は、除菌が成功したかを確認するための追加検査が必要になります。</p>

        <h2>予防・対策</h2>

        <p>H. pylori感染の予防には、良好な衛生習慣が重要です。手洗いを徹底し、食物は適切に調理し、安全な水を飲むことが推奨されます。また、家族内での感染が疑われる場合は、家族全員の検査と治療を検討することが有効です。</p>

